public class MyMath {
    public static final int x = 5;
    public static final int y = 6;
    public static final int z = 7;

    public static int addition(int x, int y, int z){
        return x+y+z;
    }

    public static int subtraction(int x, int y, int z) {
        return x-y-z;
    }

    public static int multiplication(int x, int y, int z) {
        return x*y*z;
    }
}
